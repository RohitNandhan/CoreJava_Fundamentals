package PracticePrograms.coreJava;

import java.lang.Package.*;
public class EscapeSequence {
	EscapeSequence()
	{
		
		// \n- new line 
		System.out.println("line 1\n line 2");
		// \t - tab space 
		System.out.println("hai:\thello world");
		
		// \' \"
		System.out.println("Lee\'s \"Empty ur cup to fill it again\"");
		
		//unicode for rupees
		System.out.println("\u20B9100000");
		
		// \b
		System.out.println(" Stay till u falls \\");
	}
}
